export const getPopularStars = (limit = 5) => {
  // Sorting by a 'usageCount' property which is assumed to exist on the data
  // This can't be pre-computed if usageCount is dynamic.
  // If it's static, it could be pre-sorted.
  // For now, keeping the original logic as it implies dynamic sorting.
  return [...starDetailsData]
    .sort((a, b) => (b.usageCount || 0) - (a.usageCount || 0))
    .slice(0, limit)
}
