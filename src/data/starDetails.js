// --- Pre-computed data for performance ---
const precomputedData = (() => {
  const dataById = new Map()
  const dataBySlug = new Map()
  const starsByCategory = new Map()
  const categories = new Map()

  for (const star of starDetailsData) {
    // Index by ID and Slug
    dataById.set(star.id, star)
    dataBySlug.set(star.slug, star)

    // 处理 categories 数组（支持多分类）
    const starCategories = Array.isArray(star.categories) ? star.categories : [star.categories]

    for (const category of starCategories) {
      const categorySlug = category.toLowerCase()

      // Group by Category
      if (!starsByCategory.has(categorySlug)) {
        starsByCategory.set(categorySlug, [])
      }
      starsByCategory.get(categorySlug).push(star)

      // Aggregate Category Info
      if (!categories.has(categorySlug)) {
        categories.set(categorySlug, {
          name: category,
          slug: categorySlug,
          count: 0,
        })
      }
      categories.get(categorySlug).count++
    }
  }

  // Sort stars within each category by name for better organization
  for (const stars of starsByCategory.values()) {
    stars.sort((a, b) => a.name.localeCompare(b.name))
  }

  // Define custom category order for better user experience
  const categoryOrder = {
    'classic stars': 1, // Classic stars (★, ☆) - most common
    'four-pointed-stars': 2, // four-pointed-stars (✦, ✧, ✩, etc.)
    'asterisk-and-multi-pointed': 3, // Asterisk and multi-pointed stars (✢, ✥, ✳, etc.)
    'religious-and-cultural': 4, // Religious and cultural symbols (✡, ۞, ᕯ)
    'emoji-stars': 5, // emoji-stars (🔯, ⭐, 🌟, etc.)
    'math & symbols': 6, // Mathematical symbols
    'other symbols': 7, // Other miscellaneous symbols
    'combining marks': 8, // Combining marks
    'mathematical symbols': 9, // Mathematical symbols
    emoji: 10, // General emoji
    other: 99, // Any other categories
  }

  // Sort categories by custom order, then alphabetically for any not in the order
  const sortedCategories = Array.from(categories.values()).sort((a, b) => {
    const orderA = categoryOrder[a.slug] || 50
    const orderB = categoryOrder[b.slug] || 50

    if (orderA !== orderB) {
      return orderA - orderB
    }

    // If same order priority, sort alphabetically
    return a.name.localeCompare(b.name)
  })

  return {
    dataById,
    dataBySlug,
    starsByCategory,
    categories: sortedCategories,
  }
})()

